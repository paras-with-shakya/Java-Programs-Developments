public class PS_21_Do_while {
    public static void main(String[] args) {
        int a = 0;
        do {
            System.out.println(a);
            a++;
        } while (a < 5);

        System.out.println("End of first do-while loop");

        // problem 1:
        int b = 0;
        do {
            System.out.println(b);
            b++;
        } while (b < 50);
    }
}
